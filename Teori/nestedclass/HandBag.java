package nestedclass;

public class HandBag {
  String merk;
  int harga;
  int jumlah;
  
  public void setHarga(int harga) {
    this.harga = harga;
  }
  public int getHarga() {
    return harga;
  }

  public int getJumlah() {
    return jumlah;
  }
  public void setJumlah(int jumlah) {
    this.jumlah = jumlah;
  }

  public String getMerk() {
    return merk;
  }
  public void setMerk(String merk) {
    this.merk = merk;
  }
}
